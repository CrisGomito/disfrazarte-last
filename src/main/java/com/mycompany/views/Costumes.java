    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        DAODisfraces dao = new DAODisfracesImpl();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if (jTable1.getSelectedRows().length < 1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Debe seleccionar uno o más disfraces a eliminar.\n",
                    "AVISO", javax.swing.JOptionPane.WARNING_MESSAGE);
            jTable1.requestFocusInWindow();
        } else {
            int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                    "¿Estás seguro de que deseas eliminar los disfraces seleccionados?",
                    "Confirmar eliminación",
                    javax.swing.JOptionPane.YES_NO_OPTION,
                    javax.swing.JOptionPane.WARNING_MESSAGE);

            if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                for (int i : jTable1.getSelectedRows()) {
                    try {
                        dao.eliminar((int) jTable1.getValueAt(i, 0));
                        model.removeRow(i);
                    } catch (Exception e) {
                        System.out.println(e.getMessage());
                    }
                }
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Disfraces eliminados correctamente.",
                        "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (jTable1.getSelectedRow() > -1) {
            try {
                int dsfrId = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                DAODisfraces dao = new DAODisfracesImpl();
                Dashboard.ShowJPanel(new UpDisfraces(dao.getDsfrById(dsfrId), null));
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar el disfraz a editar.\n", "AVISO", javax.swing.JOptionPane.WARNING_MESSAGE);
            jTable1.requestFocusInWindow();
        }
    }//GEN-LAST:event_editButtonActionPerformed

