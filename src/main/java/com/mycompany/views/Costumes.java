        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar primero un disfraz.", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int disfrazId = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());

        Dashboard.ShowJPanel(new AssignUsers(disfrazId));
