    private void LoadCostumes() {
        try {
            DAODisfracesImpl dao = new DAODisfracesImpl();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            dao.listarConUsuariosDirectos("").forEach((u) -> model.addRow(new Object[]{
                u.getId(),
                u.getTitle(),
                u.getTematica(),
                u.getDescription(),
                u.getTalla(),
                u.getStock(),
                u.getAvailable(),
                u.getEstado(),
                (u.getUsuariosAsignados() == null || u.getUsuariosAsignados().trim().isEmpty()) ? "-" : u.getUsuariosAsignados()
            }));

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }

