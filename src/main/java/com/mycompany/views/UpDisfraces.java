    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        String mtitle = titleTxt.getText().trim();
        String tematica = tematicaTxt.getText().trim();
        String desc = descjTextArea1.getText().trim();
        String talla = tallaTxt.getText().trim();
        String estado = (String) estadojComboBox.getSelectedItem();
        String stock = stockTxt.getText().trim();
        String disp = dispTxt.getText().trim();

        if (mtitle.isEmpty() || tematica.isEmpty() || desc.isEmpty() || talla.isEmpty()
                || estado == null || estado.isEmpty() || stock.isEmpty() || disp.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Debe llenar todos los campos obligatorios. (*) \n",
                    "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            titleTxt.requestFocus();
            return;
        } else if (!Utils.isNumeric(stock) || !Utils.isNumeric(disp)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Los campos Stock y Disponibles deben ser números enteros. \n",
                    "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            stockTxt.requestFocus();
            return;
        }

        if (!validarDisponiblesAlGuardar()) {
            return;
        }
        com.mycompany.models.Disfraces dsfr = isEdition ? dsfrEdition : new com.mycompany.models.Disfraces();
        dsfr.setTitle(mtitle);
        dsfr.setTematica(tematica);
        dsfr.setDescription(desc);
        dsfr.setTalla(talla);
        dsfr.setEstado(estado);
        dsfr.setStock(Integer.parseInt(stock));
        dsfr.setAvailable(Integer.parseInt(disp));

        try {
            DAODisfraces dao = new DAODisfracesImpl();

            if (!isEdition) {
                dao.registrar(dsfr);
            } else {
                dao.modificar(dsfr);
            }

            String successMsg = isEdition ? "modificado" : "registrado";

            javax.swing.JOptionPane.showMessageDialog(this,
                    "Disfraz " + successMsg + " exitosamente.\n",
                    "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            if (!isEdition) {
                titleTxt.setText("");
                tematicaTxt.setText("");
                descjTextArea1.setText("");
                tallaTxt.setText("");
                stockTxt.setText("");
                dispTxt.setText("");
                estadojComboBox.setSelectedIndex(0);
            }
            if (fromEventDetails && eventoOrigen != null) {
                Dashboard.ShowJPanel(new EventDetails(eventoOrigen));
            } else {
                Dashboard.ShowJPanel(new Costumes());
            }

        } catch (Exception e) {
            String errorMsg = isEdition ? "modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Ocurrió un error al " + errorMsg + " el disfraz. \n",
                    "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_buttonActionPerformed

