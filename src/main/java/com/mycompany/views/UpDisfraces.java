    private void InitStyles() {
        title.putClientProperty("FlatLaf.styleClass", "h1");
        title.setForeground(Color.black);
        titleTxt.putClientProperty("JTextField.placeholderText", "Ingrese el nombre del disfraz");
        tematicaTxt.putClientProperty("JTextField.placeholderText", "Ingrese la temática del disfraz");
        descjTextArea1.putClientProperty("JTextArea.placeholderText", "Ingrese la descripción del disfraz");
        stockTxt.putClientProperty("JTextField.placeholderText", "Ingrese el stock total");
        tallaTxt.putClientProperty("JTextField.placeholderText", "Talla");
        dispTxt.putClientProperty("JTextField.placeholderText", "Disfraces disponibles");

        estadojComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(
                new String[]{"Nuevo", "Usado", "En reparación"}
        ));

        if (isEdition && dsfrEdition != null) {
            title.setText("Editar disfraz");
            button.setText("Guardar");

            titleTxt.setText(dsfrEdition.getTitle());
            tematicaTxt.setText(dsfrEdition.getTematica());
            descjTextArea1.setText(dsfrEdition.getDescription());
            tallaTxt.setText(dsfrEdition.getTalla());
            estadojComboBox.setSelectedItem(dsfrEdition.getEstado());
            stockTxt.setText(String.valueOf(dsfrEdition.getStock()));
            dispTxt.setText(String.valueOf(dsfrEdition.getAvailable()));

            if (isEdition && dsfrEdition != null) {
                estadojComboBox.setSelectedItem(dsfrEdition.getEstado());
                dispTxt.setText(String.valueOf(dsfrEdition.getAvailable()));

                if ("En reparación".equalsIgnoreCase(dsfrEdition.getEstado())) {
                    dispTxt.setEnabled(false);
                    dispTxt.setText("0");
                } else {
                    dispTxt.setEnabled(true);
                }
            } else {
                dispTxt.setEnabled(true);
            }
        }

        estadojComboBox.addActionListener(e -> {
            String estado = (String) estadojComboBox.getSelectedItem();

            if ("En reparación".equalsIgnoreCase(estado)) {
                dispTxt.setText("0");
                dispTxt.setEnabled(false);
            } else {
                dispTxt.setEnabled(true);
                if (isEdition && dsfrEdition != null) {
                    dispTxt.setText(String.valueOf(dsfrEdition.getAvailable()));
                }
            }
        });
    }

    private boolean validarDisponiblesAlGuardar() {
        try {
            if (!dispTxt.isEnabled()) {
                return true;
            }

            int stock = Integer.parseInt(stockTxt.getText().trim());
            int disp = Integer.parseInt(dispTxt.getText().trim());

            if (disp > stock) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "El valor de Disponibles no puede ser mayor al Stock.\n",
                        "AVISO", javax.swing.JOptionPane.WARNING_MESSAGE);
                dispTxt.setText(String.valueOf(stock));
                return false;
            }
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

